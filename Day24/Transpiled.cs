	static int ReadDigit_Helper1(int d, int z, int a, int b)
	{
		if (d != z % 26 + a)
		{
			return z * 26 + d + b;
		}
		return z;
	}

	static int ReadDigit_Helper2(int d, int z, int a, int b)
	{
		if (d != z % 26 + a)
		{
			return z / 26 * 26 + d + b;
		}
		return z / 26;
	}

	static IEnumerable<int> ReadDigit_InverseHelper1(int z, int d, int a, int b)
	{
		var result = new HashSet<int>
		{
			(z - b - d) / 26,
			z,
		};
		var tmp = result.ToList();
		foreach (var r in tmp)
		{
			if (ReadDigit_Helper1(d, r, a, b) != z)
			{
				result.Remove(r);
			}
		}
		return result;
	}

	static IEnumerable<int> ReadDigit_InverseHelper2(int z, int d, int a, int b)
	{
		var result = new HashSet<int>();
		for (var i = -25; i <= 25; i++)
		{
			result.Add((z - b - d) / 26 * 26 + i);
			result.Add(z * 26 + i);
		}
		var tmp = result.ToList();
		foreach (var r in tmp)
		{
			if (ReadDigit_Helper2(d, r, a, b) != z)
			{
				result.Remove(r);
			}
		}
		return result;
	}

