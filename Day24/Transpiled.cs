	internal static void Test()
	{
		const int range = 5000;
		for (var i = 0; i < _digitSolvers.Count; i++)
		{
			for (var d = 1; d <= 9; d++)
			{
				for (var z = -range; z <= range; z++)
				{
					var z1 = _digitSolvers[i](d, z);
					var z2 = _digitSolvers_Manual[i](d, z);
					if (z1 != z2)
					{
						throw new Exception($"No match for 'ReadDigit_{i}_Manual' with input w: {d}, z: {z} - ({z1} != {z2}).");
					}
					var results = _digitSolvers_Inverse[i](z1, d).ToList();
					if (!results.Any(r => r == z))
					{
						throw new Exception($"Inverse failed for 'ReadDigit_{i}_Manual' with input d: {d}, z: {z1} (expected: {z}).");
					}
				}
			}
		}
	}

	internal static (List<int> Largest, List<int> Smallest) Solve()
	{
		static bool Validate(List<int> digits)
		{
			var i = 0;
			var z = 0;
			foreach (var d in digits)
			{
				z = _digitSolvers[i++](d, z);
			}
			return z == 0;
		}
		
		static List<int>? SolveDigit(List<Func<int, int, IEnumerable<int>>> digitSolvers, int pos, int candidate, List<int> numbers, bool findLargest, HashSet<string> cache)
		{
			var fingerPrint = $"{pos}/{candidate}";
			if (cache.Contains(fingerPrint))
			{
				return null;
			}
			cache.Add(fingerPrint);

			var from = 1;
			var to = 10;
			var inc = 1;
			if (findLargest)
			{
				from = 9;
				to = 0;
				inc *= -1;
			}

			for (var d = from; d != to; d += inc)
			{
				var results = digitSolvers[pos](candidate, d).ToList();
				foreach (var z in results)
				{
					var newNumbers = numbers.ToList();
					newNumbers.Add(d);

					if (pos == 0)
					{
						if (z == 0)
						{
							newNumbers.Reverse();
							if (Validate(newNumbers))
							{
								return newNumbers;
							}
						}
					}
					else
					{
						var digits = SolveDigit(digitSolvers, pos - 1, z, newNumbers, findLargest, cache);
						if (digits != null)
						{
							return digits;
						}
					}
				}
			}
			return null;
		}

		return
		(
			SolveDigit(_digitSolvers_Inverse, 13, 0, new(), true, new())!,
			SolveDigit(_digitSolvers_Inverse, 13, 0, new(), false, new())!
		);
	}
